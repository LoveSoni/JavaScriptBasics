'use strict'

// while loop

let i = 1;
while (i <= 10) {
    console.log(i);
    i++;

}

// array with while loop
const myHeroes = ['spiderman', 'thor', 'superman']

let j = 0;
while (j < myHeroes.length) {
    console.log(myHeroes[j]);
    j++;
}


// do loop
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 10);